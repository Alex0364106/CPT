// Loop through all the value in a sprite list, in this case, hammer. 
// 
// Then select which hammer will be used based on the user-input. Return the user's choice for later use
function Select (list: Image[]) {
    game.splash("Select your Hammer")
    for (let value of list) {
        hammer.setImage(value)
        pause(375)
    }
    sprites.destroy(hammer, effects.spray, 500)
    chose = game.askForNumber("1-4", 1)
    if (chose == 1 || (chose == 2 || (chose == 3 || chose == 4))) {
        hammer = sprites.create(hammer_list[chose - 1], SpriteKind.Player)
    } else {
        game.splash("Wrong Number")
        game.reset()
    }
    return chose
}
// Whenever the countdown ends, change the variable "level" by 1. 
// 
// If level 1-3, call Next
// If level = 4, win
// 
// If does not meet the requirements, lose.
info.onCountdownEnd(function () {
    level += 1
    if (level == 1 && info.score() >= 4) {
        Next()
    } else if (level == 2 && info.score() >= 8) {
        Next()
    } else if (level == 3 && info.score() >= 24) {
        Next()
    } else if (level == 4) {
        game.gameOver(true)
    } else {
        game.gameOver(false)
    }
})
